// Helper function to find the currently playing track based on timestamp
export function findCurrentTrack(station, currentTime) {
  if (!station || !currentTime) return null
  
  // Convert currentTime from seconds to the format "H:MM:SS"
  const hours = Math.floor(currentTime / 3600)
  const minutes = Math.floor((currentTime % 3600) / 60)
  const seconds = Math.floor(currentTime % 60)
  
  const timeString = `${hours}:${minutes.toString().padStart(2, '0')}:${seconds.toString().padStart(2, '0')}`
  
  // Parse the start and end times and convert to seconds for comparison
  for (const track of station.tracks) {
    const startParts = track.start.split(':').map(Number)
    const endParts = track.end.split(':').map(Number)
    
    const startSeconds = (startParts[0] || 0) * 3600 + (startParts[1] || 0) * 60 + (startParts[2] || 0)
    const endSeconds = (endParts[0] || 0) * 3600 + (endParts[1] || 0) * 60 + (endParts[2] || 0)
    
    if (currentTime >= startSeconds && currentTime < endSeconds) {
      return track
    }
  }
  
  return null
}

// Get random track from station
export function getRandomTrack(station) {
  if (!station || !station.tracks || station.tracks.length === 0) return null
  const randomIndex = Math.floor(Math.random() * station.tracks.length)
  return station.tracks[randomIndex]
}

// Get start time in seconds for a track
export function getTrackStartTimeInSeconds(track) {
  if (!track || !track.start) return 0
  
  const startParts = track.start.split(':').map(Number)
  return (startParts[0] || 0) * 3600 + (startParts[1] || 0) * 60 + (startParts[2] || 0)
} 